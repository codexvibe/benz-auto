-- Table des codes promos (Marketing Vectors)
CREATE TABLE IF NOT EXISTS promo_codes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code TEXT NOT NULL UNIQUE,
  discount_amount TEXT NOT NULL,
  discount_type TEXT DEFAULT 'percentage',
  expires_at TIMESTAMPTZ,
  is_active BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Table des logs système
CREATE TABLE IF NOT EXISTS admin_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  action TEXT NOT NULL,
  target TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);


-- ============================================================
-- PARTIE 2 : SÉCURITÉ RLS (Row Level Security)
-- ============================================================

ALTER TABLE products ENABLE ROW LEVEL SECURITY;
ALTER TABLE videos ENABLE ROW LEVEL SECURITY;
ALTER TABLE orders ENABLE ROW LEVEL SECURITY;
ALTER TABLE admins ENABLE ROW LEVEL SECURITY;
ALTER TABLE site_settings ENABLE ROW LEVEL SECURITY;
ALTER TABLE promo_codes ENABLE ROW LEVEL SECURITY;
ALTER TABLE admin_logs ENABLE ROW LEVEL SECURITY;

-- Suppression des anciennes politiques
DO $$ 
BEGIN
    DROP POLICY IF EXISTS "Public Read" ON products;
    DROP POLICY IF EXISTS "Admin All Products" ON products;
    DROP POLICY IF EXISTS "Public Read Videos" ON videos;
    DROP POLICY IF EXISTS "Admin All Videos" ON videos;
    DROP POLICY IF EXISTS "Anyone Insert Orders" ON orders;
    DROP POLICY IF EXISTS "Admin All Orders" ON orders;
    DROP POLICY IF EXISTS "Public Read Admins" ON admins;
    DROP POLICY IF EXISTS "Public Read Settings" ON site_settings;
    DROP POLICY IF EXISTS "Admin All Settings" ON site_settings;
    DROP POLICY IF EXISTS "Public Read Promos" ON promo_codes;
    DROP POLICY IF EXISTS "Admin All Promos" ON promo_codes;
    DROP POLICY IF EXISTS "Admin All Logs" ON admin_logs;
EXCEPTION WHEN OTHERS THEN NULL;
END $$;

-- Création des nouvelles politiques OSIRIS
CREATE POLICY "Public Read" ON products FOR SELECT USING (true);
CREATE POLICY "Admin All Products" ON products FOR ALL USING (true);

CREATE POLICY "Public Read Videos" ON videos FOR SELECT USING (true);
CREATE POLICY "Admin All Videos" ON videos FOR ALL USING (true);

CREATE POLICY "Anyone Insert Orders" ON orders FOR INSERT WITH CHECK (true);
CREATE POLICY "Admin All Orders" ON orders FOR ALL USING (true);

CREATE POLICY "Public Read Admins" ON admins FOR SELECT USING (true);

CREATE POLICY "Public Read Settings" ON site_settings FOR SELECT USING (true);
CREATE POLICY "Admin All Settings" ON site_settings FOR ALL USING (true);

CREATE POLICY "Public Read Promos" ON promo_codes FOR SELECT USING (true);
CREATE POLICY "Admin All Promos" ON promo_codes FOR ALL USING (true);

CREATE POLICY "Admin All Logs" ON admin_logs FOR ALL USING (true);


