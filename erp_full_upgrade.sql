-- Table des véhicules (Ressources)
CREATE TABLE IF NOT EXISTS products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  name TEXT NOT NULL,
  brand TEXT,
  category TEXT NOT NULL DEFAULT 'SUV',
  price TEXT NOT NULL,
  image_url TEXT,
  description TEXT,
  year TEXT,
  mileage TEXT,
  location TEXT DEFAULT 'Alger',
  engine TEXT,
  power TEXT,
  transmission TEXT DEFAULT 'Automatique',
  fuel TEXT DEFAULT 'Essence',
  images JSONB DEFAULT '[]'::jsonb,
  is_featured BOOLEAN DEFAULT FALSE,
  is_visible BOOLEAN DEFAULT TRUE,
  status TEXT DEFAULT 'Disponible',
  views_count BIGINT DEFAULT 0
);

-- Table des médias (Archives Visuelles)
CREATE TABLE IF NOT EXISTS videos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  title TEXT NOT NULL,
  thumbnail TEXT NOT NULL,
  platform TEXT DEFAULT 'youtube',
  views TEXT,
  duration TEXT,
  video_url TEXT NOT NULL
);

-- Table des leads / commandes
CREATE TABLE IF NOT EXISTS orders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  customer_name TEXT NOT NULL,
  customer_phone TEXT NOT NULL,
  items_list TEXT,
  status TEXT DEFAULT 'En attente'
);

-- Table des administrateurs (Kernel Access)
CREATE TABLE IF NOT EXISTS admins (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  passcode TEXT NOT NULL UNIQUE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

